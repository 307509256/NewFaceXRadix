package com.yxkj.facexradix.star;

import java.util.List;

public class MobPushBean {






    private String appkey;
    private String content;
    private int target;
    private int type;
    private List<Integer> plats;
    private List<String> registrationIds;
    private String data;
    private String extras;
    private int iosProduction;


    public String getExtras() {
        return extras;
    }

    public void setExtras(String extras) {
        this.extras = extras;
    }

    public int getIosProduction() {
        return iosProduction;
    }

    public void setIosProduction(int iosProduction) {
        this.iosProduction = iosProduction;
    }




    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getAppkey() {
        return appkey;
    }

    public void setAppkey(String appkey) {
        this.appkey = appkey;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getTarget() {
        return target;
    }

    public void setTarget(int target) {
        this.target = target;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }



    public List<Integer> getPlats() {
        return plats;
    }

    public void setPlats(List<Integer> plats) {
        this.plats = plats;
    }

    public List<String> getRegistrationIds() {
        return registrationIds;
    }

    public void setRegistrationIds(List<String> registrationIds) {
        this.registrationIds = registrationIds;
    }


















}
